package com.ogzcm.dto;

public class Success {
	private boolean success;
	
	public Success() {
		super();
	}

	public Success(boolean success) {
		super();
		this.success = success;
	}

	public boolean isSuccess() {
		return success;
	}

	public void setSuccess(boolean success) {
		this.success = success;
	}
}
